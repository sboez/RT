#include "libft.h"

int		ft_cnt_word(char *str, char c)
{
	int		n;
	int		word;

	n = 0;
	word = 0;
	while (str[n])
	{
		if (str[n] != c && (str[n + 1] == c || str[n + 1] == '\0'))
			word++;
		n++;
	}
	return (word);
}
